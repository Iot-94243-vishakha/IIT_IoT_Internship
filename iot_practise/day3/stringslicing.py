s = input("Enter a string: ")
print("Original String:",s)
print("First 5 characters:",s[0:5])#first 5 letters
print("Character from index 2 to 7:",s[2:8])#letters from 2nd letter to 7th letter
print("Last 5 characters:",s[-5:])
print("String without first & last character:",s[1:-1])
print("Every second character:",s[::2])
print("Reverse string:",s[::-1])
